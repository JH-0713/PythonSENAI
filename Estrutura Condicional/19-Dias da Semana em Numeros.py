print("Solicite um numero de 1 a 7 para saber que dia da semana é hoje:")
x1 = int(input("Numero = "))

if x1 == 1:
    print("Hoje é Domingo")
elif x1 == 2:
    print("Hoje é Segunda Feira")
elif x1 == 3:
    print("Hoje é Terça Feira")
elif x1 == 4:
    print("Hoje é Quarta Feira")
elif x1 == 5:
    print("Hoje é Quinta Feira")
elif x1 == 6:
    print("Hoje é Sexta Feira")
elif x1 == 7:
    print("Hoje é Sabado")
else:
    print("Erro")